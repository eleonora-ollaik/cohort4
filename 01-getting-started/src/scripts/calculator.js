const calculator = {


    add: (num1, num2) => {
        let sum = num1 + num2;
        return sum;
    },

    substract: (num1, num2) => {
        let substr = num1 - num2;
        return substr;

    },

    multiply: (num1, num2) => {
        let mult = num1*num2;
        return mult;
    },

    divide: (num1, num2) => {
        let div = num1/num2;
        return div;
    },

   
};

export default calculator;